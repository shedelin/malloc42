#include "libft.h"

int		ft_atoi(const char *s)
{
	int		nb;
	int		sign;
	size_t	i;

	sign = 1;
	i = 0;
	nb = 0;
	if (!s)
		return (0);
	while ((s[i] == ' ' || s[i] == '\n' || s[i] == '\v' || s[i] == '\t' \
			|| s[i] == '\r' || s[i] == '\f') && s[i] != '\0')
		i++;
	if (*(s + i) == '-' || *(s + i) == '+')
	{
		sign = (*(s + i) == '-' ? -1 : 1);
		i++;
	}
	while (*(s + i) >= '0' && *(s + i) <= '9')
	{
		nb *= 10;
		nb += *(s + i) - '0';
		i++;
	}
	return (nb * sign);
}
