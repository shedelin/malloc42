#include <string.h>
#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;

	i = 0;
	if (s != NULL)
	{
		while (i < n)
		{
			*((char *)s + i) = 0;
			i++;
		}
	}
}
